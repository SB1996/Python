# while Loop....!
'''
syntax : while expression :
            statement(s)

'''

# Python program to illustrate
# while loop
count = 0
while (count <= 10):
    print(count, end=" ")
    count = count + 1




# Using else statement with while loops ....!
'''
while condition : 
     execute these statements 
else : 
     execute these statements
'''
#Python program to illustrate
# combining else with while
count = 0
print()
while (count < 10):
    print(count, end=" ")
    count = count + 1
else:
    print("\nLoop end.")




