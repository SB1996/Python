# 3. Logical operators: Logical operators perform Logical AND, Logical OR and Logical NOT operations.

data00 = True
data01 = False

# and Operator ...!
print(f"AND Operatot[data00 and data01] : {data00 and data01}")

# or Operator ...!
print(f"OR Operatot[data00 or data01] : {data00 or data01}")

# not Operator ...!
print(f"NOT Operatot[not data01] : {not data01}")