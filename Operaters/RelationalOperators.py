# 2. Relational Operators : Relational operators compares the values. It either returns True or False according to the condition.

# Examples of Relational Operators
data00 = 13
data01 = 13

# data00 > data01 is False
print(f"Greater than : {data00} > {data01} : {data00 > data01}")

# data00 >= data01 is False
print(f"Greater than or equal ton : {data00} >= {data01} : {data00 >= data01}")

# data00 < data01 is False
print(f"Less than : {data00} < {data01} : {data00 < data01}")

# data00 <= data01 is False
print(f"Less than or equal to : {data00} <= {data01} : {data00 == data01}")

# data00 == data01 is False
print(f"Equal to : {data00} == {data01} : {data00 == data01}")

# data00 == data01 is False
print(f"Not equal to : {data00} != {data01} : {data00 == data01}")

