# 1. Arithmetic operators: Arithmetic operators are used to perform mathematical operations like addition,
#    subtraction, multiplication and division.

# Examples of Arithmetic Operator
data00 = 9
data01 = 2

# Addition of numbers
addition = data00 + data01
print(f"Addition[{data00} and {data01}] : {addition}")

# Subtraction of numbers
subtraction = data00 - data01
print(f"Subtraction[{data00} and {data01}] : {subtraction}")

# Multiplication of number
multiplication = data00 * data01
print(f"Multiplication[{data00} and {data01}] : {multiplication}")

# Division(float) of number
division = data00 / data01
print(f"Division[{data00} and {data01}] : {division}")

# Division(floor) of number
division = data00 // data01
print(f"Division[{data00} and {data01}] : {division}")

# Modulo of both number
modulo = data00 % data01
print(f"Modulo[{data00} and {data01}] : {modulo}")

# Exponantion of both number
exponantion = data00 ** data01
print(f"Exponantion[{data00} and {data01}] : {exponantion}")
