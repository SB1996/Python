# Assigning Number to a variable...
dataInt = 10 # 'int' Datatype
print(f"dataInt : {type(dataInt)}")

dataFloat = 10.00# 'float' Datatype
print(f"dataFloat : {type(dataFloat)}")

dataComplex = complex(10,9)
dataComplex00 = 10+9j
print(f"dataComplex : {type(dataComplex)}")
print(f"dataComplex00 : {type(dataComplex00)}")

# Assigning Boolean to a variable...
dataBoolean = True# 'bool' Datatype
print(f"dataString : {type(dataBoolean)}")

# Assigning String/Character to a variable...
dataString = "Santanu"# 'str' Datatype
print(f"dataString : {type(dataString)}")
