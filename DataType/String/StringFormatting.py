name = "Santanu Banik"

# Using String concatenation...
strFormat = "Hello Mr."+name+", i'm Python interpreter."
print(strFormat)

# Using Interpolation...
strFormat = f"Hello Mr.{name}, i'm Python interpreter."
print(strFormat)

# Using %
strFormat = "Hello Mr.%s, i'm Python interpreter."%(name)
print(strFormat)



