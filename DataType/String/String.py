# Python Program for Declaring of String

# Creating a String with single Quotes...
string00 = 'Hiiii I am Python, And you'
print(f"String00 : {string00}")

# Creating a String with double Quotes
string01 = " Python : Hiiii I'm Python, And you\n\t\t\tMe : Santanu Banik"
print(f"String01 : {string01}")

# Creating a String with triple Quotes
string02 = '''Python : Hiiii I'm Python, And you\n\t\t\tMe : Santanu Banik'''
print(f"string02 : {string02}")

# Creating String with triple Quotes allows multiple lines
string03 = '''Python : Hiiii I'm Python, And you
           Me : Santanu Banik'''
print(f"string03 : {string03}")

# Using raw String to ignore Escape Sequences
string04 = R"Python : Hiiii I'm Python, And you\n\t\t\tMe : Santanu Banik"
print(f"string04 : {string04}")