# Polymorphism in Python ....!
#
# Polymorphism : The word polymorphism means having many forms. In programming, polymorphism means same function name
#                (but different signatures) being uses for different types.
#
# Plymorphism : 1. Dynamic typing.
#               2. Duck typing.
#               3. method overloading.(Not support in python)
#               4. operater overloading.
#               5. method overriding.



# Dynamic typing in Python....
data = 10
print(f"type of data : {type(data)}")
print(f"id of data : {id(data)}")
print(f"value of data : {data}")

print("______________________________________")

# change the type of data(Dynamic typeing)
data = "Santanu"
print(f"type of data : {type(data)}")
print(f"id of data : {id(data)}")
print(f"value of data : {data}")
